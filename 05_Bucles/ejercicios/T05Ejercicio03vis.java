/**
 * Imprime todos los multiplos de 5 del 0 al 100 con bucle do while, corrección de clase
 * 
 * @author Antonio Sánchez
 */
/**
 * T05Ejercicio03vis
 */
public class T05Ejercicio03vis {
  public static void main(String[] args) {
    int i = 5;
    do {
      System.out.println(i);
      i += 5;
    } while (i <= 100);
  }
}