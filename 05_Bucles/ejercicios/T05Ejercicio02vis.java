/**
 * Imprime todos los multiplos de 5 del 0 al 100 con bucle while, corrección de clase
 * 
 * @author Antonio Sánchez
 */
/**
 * T05Ejercicio02
 */
public class T05Ejercicio02vis {
  public static void main(String[] args) {
    int i = 5;
    while (i <= 100) {
      System.out.println(i);
      i += 5;
    }
  }
}